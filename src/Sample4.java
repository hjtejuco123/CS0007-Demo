public class Sample4 {
    public static void main(String[] args) {
        //increment and decrement operators
        int x=5;
        //postfix increment
        System.out.println("Value1: "+(x++));
        System.out.println("Result1: "+x);
        //prefix increment
        System.out.println("Value2: "+(++x));
        System.out.println("Result2: "+x);
        //postfix decrement
        System.out.println("Value3: "+(x--));
        System.out.println("Result3: "+x);
        //prefix decrement
        System.out.println("Value4: "+(--x));
        System.out.println("Result4: "+x);
        
    }
}
